        private readonly CSDLGiaDBContext _db;
        private readonly ITrangThaiHoSoService _trangThaiHoSoService;
        public GiaThueMuaNhaXhXdController(CSDLGiaDBContext db, ITrangThaiHoSoService trangThaiHoSoService)
        {
            _db = db;
            _trangThaiHoSoService = trangThaiHoSoService;
        }

        [Route("GiaThueMuaNhaXhXd/XetDuyet")]
        [HttpGet]
        public IActionResult Index(string Madv, int Nam)
        {
            if (!string.IsNullOrEmpty(HttpContext.Session.GetString("SsAdmin")))
            {
                if (Helpers.CheckPermission(HttpContext.Session, "csdlmucgiahhdv.dinhgia.thuemuanha.xetduyet", "Index"))
                {

                    List<string> list_trangthai = new List<string> { "CD", "HT", "DD", "CB" };
                    var model = _db.GiaThueMuaNhaXh.Where(t => list_trangthai.Contains(t.Trangthai));
                    if (Nam != 0)
                    {
                        model = model.Where(t => t.Thoidiem.Year == Nam);
                    }

                    var model_join = (from dg in model
                                      join donvi in _db.DsDonVi on dg.Madv equals donvi.MaDv
                                      select new CSDLGia_ASP.Models.Manages.DinhGia.GiaThueMuaNhaXh
                                      {
                                          Id = dg.Id,
                                          Madv = dg.Madv,
                                          Mahs = dg.Mahs,
                                          Thoidiem = dg.Thoidiem,
                                          Soqd = dg.Soqd,
                                          Thongtin = dg.Thongtin,
                                          TenDonVi = donvi.TenDv,
                                          Trangthai = dg.Trangthai
                                      });

                    ViewData["Madv"] = Madv;
                    ViewData["Nam"] = Nam;
                    ViewData["Title"] = "Thông tin xét duyệt giá thuê mua nhà xã hội";
                    ViewData["MenuLv1"] = "menu_dg";
                    ViewData["MenuLv2"] = "menu_dgtmnxh";
                    ViewData["MenuLv3"] = "menu_dgtmnxh_xd";
                    return View("Views/Admin/Manages/DinhGia/GiaThueMuaNhaXh/XetDuyet/Index.cshtml", model_join);

                }
                else
                {
                    ViewData["Messages"] = "Bạn không có quyền truy cập vào chức năng này!";
                    return View("Views/Admin/Error/Page.cshtml");
                }
            }
            else
            {
                return View("Views/Admin/Error/SessionOut.cshtml");
            }
        }

        [HttpPost]
        public IActionResult Duyet(string mahs_duyet)
        {
            if (!string.IsNullOrEmpty(HttpContext.Session.GetString("SsAdmin")))
            {
                if (Helpers.CheckPermission(HttpContext.Session, "csdlmucgiahhdv.dinhgia.thuemuanha.xetduyet", "Approve"))
                {
                    var model = _db.GiaThueMuaNhaXh.FirstOrDefault(p => p.Mahs == mahs_duyet);

                    model.Updated_at = DateTime.Now;
                    model.Trangthai = "DD";

                    _db.GiaThueMuaNhaXh.Update(model);
                    _db.SaveChanges();

                    //Add Log
                    _trangThaiHoSoService.LogHoSo(model.Mahs, Helpers.GetSsAdmin(HttpContext.Session, "Name"), "Duyệt");
                    return RedirectToAction("Index", "GiaThueMuaNhaXhXd", new { Nam = model.Thoidiem.Year });
                }
                else
                {
                    ViewData["Messages"] = "Bạn không có quyền truy cập vào chức năng này!";
                    return View("Views/Admin/Error/Page.cshtml");
                }
            }
            else
            {
                return View("Views/Admin/Error/SessionOut.cshtml");
            }
        }

